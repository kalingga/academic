public class Lingkaran
{
	public static void main(String[] args)
	{
		int jari=10;
		double luas, keliling;
		
        luas=3.14*(jari^2);
        keliling=2*3.14*jari;
        
		System.out.println("Jari-jari: " + jari);
		System.out.println("------------------");
		System.out.println("Luas     : " + luas);
		System.out.println("Keliling : " + keliling);
	}
}
